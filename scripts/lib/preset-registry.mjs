import { readFileSync, writeFileSync, existsSync } from 'node:fs'
import { resolve, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
export const ROOT = resolve(__dirname, '../..')
export const REGISTRY_PATH = resolve(ROOT, 'src/design/presets/registry.json')
export const VARS_DIR = resolve(ROOT, 'src/design/presets/vars')
export const VARS_INDEX_PATH = resolve(VARS_DIR, 'index.css')
export const APP_CSS_PATH = resolve(ROOT, 'src/styles/app.css')

export function loadRegistry() {
  return JSON.parse(readFileSync(REGISTRY_PATH, 'utf8'))
}

export function saveRegistry(registry) {
  writeFileSync(REGISTRY_PATH, `${JSON.stringify(registry, null, 2)}\n`)
}

export function syncVarsIndex(registry = loadRegistry()) {
  const imports = registry.presets
    .map((preset) => `@import "./${preset.cssFile}";`)
    .join('\n')

  const content = `/* AUTO-GENERATED by scripts/sync-preset-registry.mjs — do not edit */\n${imports}\n`
  writeFileSync(VARS_INDEX_PATH, content)
  return VARS_INDEX_PATH
}

export function ensureAppCssImportsRegistry(registry = loadRegistry()) {
  const appCss = readFileSync(APP_CSS_PATH, 'utf8')
  const registryImport = '@import "../design/presets/vars/index.css";'

  let next = appCss
  const perPresetImport = /@import "\.\.\/design\/presets\/vars\/[^"]+\.css";?\n/g
  if (perPresetImport.test(appCss)) {
    next = appCss.replace(perPresetImport, '')
  }

  if (!next.includes(registryImport)) {
    next = next.replace(
      '@import "@fontsource-variable/inter";',
      `@import "@fontsource-variable/inter";\n${registryImport}`,
    )
  }

  next = next.replace(/\n{3,}/g, '\n\n')
  writeFileSync(APP_CSS_PATH, next)
}

export function upsertPreset(registry, preset) {
  const index = registry.presets.findIndex((entry) => entry.id === preset.id)
  if (index === -1) {
    registry.presets.push(preset)
  } else {
    registry.presets[index] = { ...registry.presets[index], ...preset }
  }
  return registry
}

export function getPresetIds(registry = loadRegistry()) {
  return registry.presets.map((preset) => preset.id)
}

export function resolvePresetForIndustry(registry, industry) {
  const normalized = industry.toLowerCase()
  const match = registry.presets.find((preset) =>
    preset.industries.some(
      (tag) => normalized.includes(tag) || tag.includes(normalized),
    ),
  )
  return match?.id ?? registry.defaultPreset
}
